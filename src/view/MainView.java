    public void setSelection(List<String> selection) {
        if (selection.isEmpty()) {
            selectionLabel.setText(emptySelection);
        } else {
            String text = "Current Selection: ";
            for (String s : selection) {
                text = text + s + " ";
            }
            text.trim();
            selectionLabel.setText(text);
        }
    }
    
